sample_string = "Hello, World!"

print("Length of the string:", len(sample_string))
print("Uppercase:", sample_string.upper())
print("Lowercase:", sample_string.lower())
print("Titlecase:", sample_string.title())
print("Capitalized:", sample_string.capitalize())
print("Count of 'l':", sample_string.count('l'))
print("Index of 'o':", sample_string.index('o'))
print("Replace 'Hello' with 'Hi':", sample_string.replace('Hello', 'Hi'))
print("Split by comma:", sample_string.split(','))
print("Strip leading and trailing whitespaces:", "  Hello  ".strip())
print("Check if string starts with 'H':", sample_string.startswith('H'))
print("Check if string ends with 'd!':", sample_string.endswith('d!'))
